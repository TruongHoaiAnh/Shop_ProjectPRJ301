package Models;

/**
 *
 * @author PhucND-CE171160
 */
public class ChartYear {
    private String year;
    private int total;
    private String name;

    public ChartYear() {
    }

    public ChartYear(String year, int total, String name) {
        this.year = year;
        this.total = total;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
}
